        private void timer1_Tick(object sender, EventArgs e)
        {
            int sayi = Convert.ToInt32(label1.Text) + 1;

            label1.Text = sayi.ToString();

            if (label1.Text == "6")
            {
                DevamEt frm3 = new DevamEt();
